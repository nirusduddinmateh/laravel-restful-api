     * @return \Illuminate\Http\JsonResponse
     */
    public function index(Request $request)
    {
        $keyword = $request->get('q');
        $perPage = 15;

        if (!empty($keyword)) {
            $posts = Post::query()->where('name', 'LIKE', "%$keyword%")
                ->latest()
                ->paginate($perPage);
        } else {
            $posts = Post::query()
                ->latest()
                ->paginate($perPage);
        }

        return response()->json($posts);
    }

