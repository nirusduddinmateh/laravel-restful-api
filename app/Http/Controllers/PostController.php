     * Remove the specified resource from storage.
     *
     * @param int $id
     * @return Application|Redirector|RedirectResponse
     */
    public function destroy($id)
    {
        $post = Post::query()->findOrFail($id);
        Storage::disk('public')->delete($post->img);
        $post->delete();
